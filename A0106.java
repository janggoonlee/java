
public class A0106 {

	public static void main(String[] args) {

		System.out.println("Hello!");
		System.out.println(4);

		
		//printf = 출력시 서식 필요
		System.out.printf("%d\n",10);
		System.out.printf("%.1f\n",4.5); //실수 소수점 기준으로 소수점 숫자 표현 
		System.out.printf("%c\n",'a'); // 하나의 글자 '' 들어감
		System.out.printf("%s\n","aa"); // 문자열 "" 들어감
	}

}
