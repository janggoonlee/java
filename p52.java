package JavaBeginnerStart;

public class p52 {

	public static void main(String[] args) {

		// p52 Java 프로그램 작성 
		// 변수: 무언가를 담는그릇, 바뀔수 있는수, 초기값 설정.
		
		int a = 100, b =50;  // int :정수,  = :대입 , 100 : 정수값 입력 (그릇에 내용물 담기.)
		int result ;
		
		
		result = a+b; // 100 과 50을 더한 값을 result 에 담는다
		System.out.println(a+"+"+b+"="+result); //  왼쪽 부터 오른쪽 까지 결과물 출력 !

	}

}
