
public class Daeb_yunesan {

	public static void main(String[] args) {
		//대입 연산자
		
		
		float a = 10f; //
		
		a++;
		System.out.printf("a ++ ==>%f\n",a ); //11
		
		a--;
		System.out.printf("a -- ==>%f\n",a );//10
		
		a += 5;
		System.out.printf("a += 5 ==>%f\n",a ); //15
		
		a -= 5;
		System.out.printf("a -= ==>%f\n",a ); // 10
		
		a *= 5;
		System.out.printf("a *= ==>%f\n",a ); // 50
		
		a /= 5;
		System.out.printf("a /= ==>%f\n",a ); //10
		
		a %= 5;
		System.out.printf("a %= ==>%f\n",a ); //0
		
		
		
		
	}

}
